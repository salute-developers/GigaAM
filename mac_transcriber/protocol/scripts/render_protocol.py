def render(template_dir=".", template_name="protocol_template.html.j2",
           out_pdf="protocol.pdf", data=DATA):
    env = Environment(
        loader=FileSystemLoader(template_dir),
        autoescape=select_autoescape(["html", "j2"]),
    )
    html = env.get_template(template_name).render(**data)
    HTML(string=html, base_url=template_dir).write_pdf(out_pdf)
    return out_pdf

